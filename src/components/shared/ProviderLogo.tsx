// Maps a catalog `provider` string (free text) to a Simple Icons brand key.
// Alibaba's Qwen models render with the Alibaba mark since Qwen has no
// separate widely-recognized brand mark of its own.
function resolveBrand(provider: string): Brand | null {
  const p = provider.toLowerCase();
  if (p.includes("openai")) return BRANDS.openai;
  if (p.includes("meta")) return BRANDS.meta;
  if (p === "google" || p.includes("google")) return BRANDS.google;
  if (p.includes("microsoft")) return BRANDS.microsoft;
  if (p.includes("alibaba") || p.includes("qwen")) return BRANDS.alibaba;
  if (p.includes("mistral")) return BRANDS.mistral;
  if (p.includes("hugging face") || p === "hugging face") return BRANDS.huggingface;
  if (p.includes("deepseek")) return BRANDS.deepseek;
  return null;
}

// Deterministic fallback color for providers without a known brand mark, so
// the same provider always gets the same chip color across the app.
const FALLBACK_COLORS = [
  "#6366F1", "#0EA5E9", "#10B981", "#F59E0B", "#EF4444",
  "#8B5CF6", "#EC4899", "#14B8A6", "#F97316", "#84CC16",
];

function hashColor(s: string): string {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return FALLBACK_COLORS[h % FALLBACK_COLORS.length];
}

function initials(s: string): string {
  const words = s.replace(/[().]/g, "").split(/\s+/).filter(Boolean);
  if (words.length === 1) return words[0].slice(0, 2).toUpperCase();
  return (words[0][0] + words[1][0]).toUpperCase();
}

export default function ProviderLogo({
  provider,
  size = 24,
}: {
  provider: string;
  size?: number;
}) {
  const brand = resolveBrand(provider);

  if (brand) {
    return (
      <div
        className="flex shrink-0 items-center justify-center overflow-hidden rounded-md"
        style={{ width: size, height: size, background: brand.bg }}
        title={provider}
      >
        <svg
          viewBox={brand.viewBox}
          width={size * 0.62}
          height={size * 0.62}
          fill={brand.fg ?? "currentColor"}
        >
          {brand.paths.map((p, i) => (
            <path key={i} d={p.d} fill={p.fill ?? brand.fg} />
          ))}
        </svg>
      </div>
    );
  }

  // Unknown provider: no fabricated logo — a clean monogram chip instead.
  const color = hashColor(provider);
  return (
    <div
      className="flex shrink-0 items-center justify-center rounded-md text-[10px] font-semibold text-white"
      style={{ width: size, height: size, background: color }}
      title={provider}
    >
      {initials(provider)}
    </div>
  );
}
